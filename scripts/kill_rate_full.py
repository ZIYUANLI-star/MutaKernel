"""Full kill-rate evaluation for L1 + L2 kernels.

Only tests kernels that PASS correctness check and selects the iteration
with the highest speedup (from best_kernels.json generated by scan_best_kernels.py).

P1 (load_inline name patch) applied.
Tolerance aligned to KernelBench native (atol=1e-2, rtol=1e-2).
No enhanced inputs — standard get_inputs() only.
Results saved incrementally to JSON for crash recovery.
"""
